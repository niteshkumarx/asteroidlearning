<div class="term1"><span style="font-size:1.50em">What is Asteroid Learning
</span><br/>
Asteroid is now an emerging initiative to bring together the people who wish
 to learn technology to our energetic Asteroid Educators using our learner’s
 network. We need to separate the boundaries between a learner and an educator
 and that's our mission irrespective of the abilities and place where the
 learner lives. We love innovation and that's all we do. At Asteroid we
 have a team of professionals, engineers, designers and developers who
 are actually part of various industries, together we build  such a solid
 base of knowledge that learning can become fun.
</div>
<div class="term2"><span style="font-size:1.50em">Who should use Asteroid
</span><br/>Anyone, yes you heard it right. We truly wish to eliminate age, gender and profession boundaries for learning. Our process of learning is simplified for everyone. you don’t have to put too much effort to learn new skills even if you’re a beginner.
</div>
<div class="term3"><span style="font-size:1.50em">How can one join Asteroid
</span><br/>Its quite simple to join asteroid, all you need to do is to signup to our online portal <b>www.asteroidlearning.com</b> and proceed to login. You can find learner’s login under Roles >> Learner. You can now personalize your profile the way you want, create classes, manage class, connect to learners, invite learners to your class, participate in other classes, buddify and even you can request to act as an educator. The social network tracks people matching to your interest and near to you and make it possible to collaborate and interact with them.
</div>
<div class="term4"><span style="font-size:1.50em">What is a Classroom
</span><br/>A classroom is your request to Asteroid Learning to organize sessions on skills at your desired location near to you. By creating the Classroom you become the Class Admin and you may invite your nearby learners to join your class through our social network. We send a confirmation link as an invitation to other learners so that there might not be any issue of discrepancy. You may choose from the infrastructure providence either by yourself or Asteroid Management will do it for you. If you’re a student then your school classroom or university classroom, labs may work with the proper written permission from the administration of your institute. For professionals seeking training, your office, meeting rooms,  leisure rooms may work as a classroom with a proper written permission from your administration. All written permissions should be uploaded by the Admin itself against the Classroom he/she created. For individuals up to few members like ten, your own home or a comfortable place with proper comfort level for learners will work as a classroom. In case you’re not able to provide the classroom Asteroid Management will do it for you with a nominal increment in per learner charges. However Class Admin will have to pay the nominal increment if caused.
</div>
<div class="term5"><span style="font-size:1.50em">What fee is charged by Asteroid
</span><br/>Our pricing policy is dynamic and decreases with increase in number of participants. It means more participant learners results to less fee. There's absolutely no fee for Class Admins and our premium members, thus if you’re the class admin of a class having candidates more than five excluding you, you pay nothing to us. A premium membership can be availed for minimum six to maximum twelve months priced at ₹10000 a month in which you can request for any skill at any place at any number of learners. Under premium validity time period one kind of skill request can be made only once. Premium membership is not applicable for Game Development and Big Data Analytics. Asteroid Learning reserves the right to change pricing slab, with prior notice to the current Class Admins. 
</div>
<div class="term6"><span style="font-size:1.50em">Fee Refund
</span><br/>If any class is cancelled due to any cause by either Class Admin or Asteroid hundred percent fee is refunded to the learners. However no fee refund is possible in case of absence of a learner in a class.
</div>
<div class="term7"><span style="font-size:1.50em">Cities where Asteroid is available
</span><br/>Your city may or may not be available as the service area of Asteroid. We’re continuously  expanding our service area, currently services of Asteroid are available in more than a thousand cities across India. Final confirmation of a request will be verified by Asteroid Management through  telephonic conversation  with the Classroom Admin. Just in case your city is not available to our autosuggested cities, then too you may request for the Classroom however the final commencement of Class is subject to the availability at new location. At any stage Asteroid reserves the right to cancel the Classroom request at any city with full refund of any payments if made.
</div>
<div class="term8"><span style="font-size:1.50em">I don’t have a Computer
</span><br/>Having a laptop or a desktop computer is not a must to avail the skills, but its highly recommended if you could arrange one. You may share the computing resources from a friend learner attending the Class. Does Asteroid provides Laptop? Yes, but only in the cities having Asteroid regional centers, at an additional cost of ₹1200 per Laptop for a class. However, at the end of the course the laptop has to be returned back to Asteroid in good conditions.
</div>
<div class="term9"><span style="font-size:1.50em">Infrastructure Requirements
</span><br/>For a Classroom, seating arrangement for all learners is essential along with power facility for laptops operation. Classroom or  Meeting room equipped with a projector and sound system is a good ideal infrastructure. How ever its not a must. But just in case the classroom size exceeds a hundred people then sound system and projector facility is highly recommended to impart complete quality learning. An institute seminar room or office seminar room is perfect to act as a classroom infrastructure. A Class Admin has the power to charge a suitable amount from participating learners for making suitable arrangements if required, Asteroid has nothing to do with in between expenditures.
</div>
<div class="term10"><span style="font-size:1.50em">Certification Examination
</span><br/>Asteroid conducts a certification exam after successful completion of the course. Its mandatory for all the learners to participate in the certification exam failing which he/she will not be awarded with the Asteroid Learning certificate. However just in case of exceptional unavoidable case a learner who has missed the certification exam can visit any Asteroid Learning regional centers across India within one year from the date of completion of course and take the certification exam after which he/she will be awarded with the Asteroid Learning certificate.
Your certificate is globally acceptable and can be verified from anywhere by quoting the AIR number of the certificate in the website of Asteroid Learning.
</div>
<div class="term11"><span style="font-size:1.50em">Can school children attend a Classroom
</span><br/>All our trainings require a basic mathematics and science knowledge, thus a student of eighth grade or higher  can attend the Asteroid Learning classrooms. However to act as a Classroom Admin the candidate should be at least 18 years old since all the financial transactions will be carried out with Classroom Admin. However since there is no fee for a Class Admin for learners above five, parents or any adult care taker may act as Classroom Admin. There are some courses which require a prerequisite knowledge of basic computer programming for such courses school children are advised to not to take part if they’re not ready with basic computer skills. However, with Asteroid its not at all essential to have a deep knowledge in any of rocket sciences.
</div>
