</span><br/>Depending upon course we provide with the soft copy of a bulk of study materials, videos, examples, workouts and slideshows developed by Asteroid for quick and effective learning. We may provide some take away referential books which are highly recommended by us. Along with study materials we do provide trial software, freeware and utility software as the softcopy to every learner. We do not provide any of these in a disk or flash media we only provide with the soft copies. None of the study material, software and videos provided by Asteroid should be replicated and pirated by any of the learner, is a punishable offence under cyber crime and shall be dealt with law. All the materials provided as a soft copy is copyright protected by their respective manufacturers. 
